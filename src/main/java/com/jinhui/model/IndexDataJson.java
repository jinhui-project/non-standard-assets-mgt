package com.jinhui.model;

public class IndexDataJson {

    private String asset;
    
    private String have_products;
    
    private String services;
    
    private String average_rate;
    
    private String products;
    
    private String funds;

	public String getAsset() {
		return asset;
	}

	public void setAsset(String asset) {
		this.asset = asset;
	}

	public String getHave_products() {
		return have_products;
	}

	public void setHave_products(String have_products) {
		this.have_products = have_products;
	}

	public String getServices() {
		return services;
	}

	public void setServices(String services) {
		this.services = services;
	}

	public String getAverage_rate() {
		return average_rate;
	}

	public void setAverage_rate(String average_rate) {
		this.average_rate = average_rate;
	}

	public String getProducts() {
		return products;
	}

	public void setProducts(String products) {
		this.products = products;
	}

	public String getFunds() {
		return funds;
	}

	public void setFunds(String funds) {
		this.funds = funds;
	}
    
}
